public class TestaLacos {
	
	public static void main(String[] args) {
		
		for (int multiplacador = 1; multiplacador <= 10; multiplacador++) {
			for (int contador = 0; contador <= 10; contador++) {
				System.out.print(multiplacador * contador);
				System.out.print(" ");
			}
			System.out.println();
		}
	}
}
